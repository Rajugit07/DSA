#include <iostream>
using namespace std;

int main()
{

     // * Bitwise Operator

     // * And Operator (&)
     cout << (3 & 5) << endl;

     // * OR Operator (|)
     cout << (3 | 5) << endl;

     // * XOR Operator (^)
     cout << (3 ^ 5) << endl;

     // * Not Operator (~)
     cout << (~6) << endl;



     // Binary Shift Operator(<< and >>)
     cout<<(7 << 2)<<endl;
     cout<<(7 >> 2)<<endl;

     return 0;
}